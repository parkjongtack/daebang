        ⑤ 회사에서 규정한 게시기간을 초과한 경우<br/>
        ⑥ 회원이 자신의 홈페이지와 게시판에 음란물을 게재하거나 음란사이트를 링크하는 경우<br/>
        ⑦ 기타 관계법령에 위반된다고 판단되는 경우
