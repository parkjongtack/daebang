        ① 게시물에 대한 권리와 책임은 게시자에게 있으며 회사는 게시자의 동의 없이는 이를 서비스내게재 이외에 영리적 목적으로 사용할 수 없습니다.<br/>
        대방열림고시학원의 모든 동 영상 강의의 저작권은 대방열림고시학원에 있으며 불법복제 및 유통시(영리적 목적이 아닌 소장용도 해당) 형사고발 조치합니다.<br/>
        ② 회원은 서비스를 이용하여 얻은 정보를 가공, 판매하는 행위등 서비스에 게재된 자료를 상업적으로 사용할 수 없습니다.<br/>
        ③ 대방열림고시학원의 동영상강의를 무단 캡쳐시도 할 경우 캡쳐시도한 자의 정보 및 증거자료는 자동으로 넘어오며, 자동 차단 및 민·형사상 고발조치 합니다.
