        <p>
        ① 서비스의 이용은 회사의 업무상 또는 기술상 특별한 지장이 없는 한 연중무휴 1일 24시간을 원칙으로 합니다. 다만 정기 점검 등의 필요로 회사가 정한 날이나 시간은 그러하지 않습니다.<br/>
        ② 회사는 서비스를 일정범위로 분할하여 각 범위별로 이용가능 시간을 별도로 정할 수 있습니다. 이 경우 그 내용을 사전에 공지합니다.
        </p>
        <h4>제 16 조 (서비스 이용 책임)</h4>
        <p>
        회원은 회사에서 권한있는 사원이 서명한 명시적인 서면에 구체적으로 허용한 경우를 제외하고는 서비스를 이용하여 불법상품을 판매하는 영업활동을 할 수 없으며 특히 해킹,돈벌기 광고,음란사이트를 통한 상업행위, 상용S/W 불법배포 등을 할 수 없습니다. 이를 어기고 발생한 영업활동의 결과 및 손실, 관계기관에 의한 구속등 법적 조치등에 관해서는 회사가 책임을 지지 않습니다.
        </p>
        <h4>제 17 조 (서비스 제공의 중지)</h4>
        <p>
        ① 회사는 다음 각 호에 해당하는 경우 서비스 제공을 중지할 수 있습니다.<br/>
        - 서비스용 설비의 보수 등 공사로 인한 부득이한 경우<br/>
        - 전기통신사업법에 규정된 기간통신사업자가 전기통신 서비스를 중지했을 경우<br/>
        - 기타 불가항력적 사유가 있는 경우<br/>
        ② 회사는 국가비상사태, 정전, 서비스 설비의 장애 또는 서비스 이용의 폭주 등으로 정상적인 서비스 이용에 지장이 있는 때에는 서비스의 전부 또는 일부를 제한하거나 정지할 있습니다.
        </p>
        <h3>제 5장 이용계약 체결</h3>
        
        <h4>제 18 조 (계약해지 및 이용제한)</h4>
        <p>
        통신판매로 구입한 상품(용역)의 경우, 14일 이내에 해약할 수 있도록 "방문판매 등에관한법률"에'청약철회권'을 규정해 놓고 있습니다.<br/>
        ※ 계약해지 희망시 내용증명은 필히 제출하여야 합니다. <br/>
        ① 철회권행사 기간의 기산점은 다음 각 호와 같습니다.<br/>
        - 상품을 인도받은 날<br/>
        - 회원증을 교부받은 때보다 상품의 인도 또는 용역 제공이 늦게 이루어진 경우에는 상품을 인도받거나 용역을 제공받은 날<br/>
        - 회원증을 교부 받지 아니하였거나 주소 등이 기재되지 아니한 회원증을 받은 경우 또는 방문판 매업자의 주소변경 등의 사유로 기간내 청약철회가<br/>
        받은 경우에는 그 주소를 안날 또는 알 수 있었던 날입니다.<br/>
        ② 소비자가 청약 철회를 할 수 없는 경우는 다음 각 호와 같습니다.<br/>
        - 소비자에게 책임있는 사유로 상품이 멸실 또는 훼손된 경우 (물품 회손시 그에 해당하는 손료는 소비자가 부담해야 함)<br/>
        - 사용 또는 일부 소비에 의하여 가치가 현저히 감소될 우려가 있는 상품 (음반.비디오물 및 소프트웨어)을 사용 또는 소비한 경우 등입니다.<br/>
        ③ 회사는 회원이 다음 각 호의 1에 해당하는 행위를 하였을경우 사전통지 없이 이용계약을 해지하거나 또는 기간을 정하여 서비스 이용을 중지할 수 있습니다.<br/>
        - 타인의 서비스 ID 및 비밀번호를 도용한 경우<br/>
        - 서비스 운영을 고의로 방해한 경우<br/>
        - 가입한 이름이 실명이 아닌 경우<br/>
        - 같은 사용자가 다른 ID로 이중등록을 한 경우<br/>
        - 공공질서 및 미풍양속에 저해되는 내용을 고의로 유포시킨 경우<br/>
        - 회원이 국익 또는 사회적 공익을 저해할 목적으로 서비스이용을 계획 또는 실행하는 경우<br/>
